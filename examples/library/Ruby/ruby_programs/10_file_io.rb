filename = "sample_output.txt"

File.open(filename, "w") do |file|
  file.puts "This file was generated by a Ruby program."
  file.puts "Line 2: Ruby makes file I/O simple."
  file.puts "Line 3: #{Time.now}"
end

puts "Wrote to #{filename}. Contents:"
puts "-" * 40

File.foreach(filename) do |line|
  puts line
end

File.delete(filename) if File.exist?(filename)
puts "-" * 40
puts "Cleaned up #{filename}"
